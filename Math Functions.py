#Math Package must be imported into the system in order for the functions to work
import math
pi=3.14
#Rounds to the nearest whole number
print(round(pi))
#The Ceil function rounds the number up to the nearest whole integer.
print(math.ceil(pi))
#The Floor function rounds a number down by the nearest integer value.
print(math.floor(pi))
#The Absolute Value function:(abs) will convert any value into the positive total
pi=-3.14
print(abs(pi))
# pow function will raise the base number to a power
print(pow(pi,2))
#Square Root (math.sqrt)
print(math.sqrt(420))
# Max will compare values to print the largest value of a set
x=2
y=4
z=-12
print(max(x,y,z))
#Lowest will compare values and return the smallest value of the given set
print(min(x,y,z))